#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	n;

	if (s)
	{
		n = 0;
		while (s[n] != '\0')
		{
			ft_putchar_fd(s[n], fd);
			n++;
		}
	}
}
